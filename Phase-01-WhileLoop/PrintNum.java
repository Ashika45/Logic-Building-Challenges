//Print all numbers from 1 to 10 using a loop. 

public class PrintNum
{
    public static void main(String [] args)
    {
        System.out.println("Printing numbers from 01 to 10");
        int i=1;
        while( i<=10)
        {
            System.out.println(i);
            i++;
        }
      
    }
}